#!/usr/bin/env python
"""Phase 2 readiness gate and launch-command generator.

This script does not run HPO or final evaluation. It validates the fixed
protocol, configuration surface, local data, exact dependencies, and smoke
tests. READY_FOR_HPO is printed only when every check succeeds.
"""

